/**

@see   DTMB_DEMOD_FP_IS_CONNECTED_TO_I2C

*/
void
realtek_r_dtmb_IsConnectedToI2c(
	DTMB_DEMOD_MODULE *pDemod,
	int *pAnswer
)
{
	U32BITS ChipId;



	// Get CHIP_ID.
	if (pDemod->RegAccess.Addr16Bit.GetRegBits(pDemod, DTMB_CHIP_ID, &ChipId) != FUNCTION_SUCCESS)
		goto error_status_get_registers;

	// Check chip ID value.
	if (ChipId != REALTEK_R_DTMB_CHIP_ID_VALUE)
		goto error_status_check_value;


	// Set I2cConnectionStatus with YES.
	*pAnswer = YES;


	return;


error_status_check_value:
error_status_get_registers:

	// Set I2cConnectionStatus with NO.
	*pAnswer = NO;


	return;
}





/**

@see   DTMB_DEMOD_FP_SOFTWARE_RESET

*/
int
realtek_r_dtmb_SoftwareReset(
	DTMB_DEMOD_MODULE *pDemod
)
{

	// Set SOFT_RST with 0x0. Then, set SOFT_RST with 0x1.
	if (pDemod->RegAccess.Addr16Bit.SetRegBits(pDemod, DTMB_SOFT_RST_N, 0x0) != FUNCTION_SUCCESS)
		goto error_status_set_registers;

	if (pDemod->RegAccess.Addr16Bit.SetRegBits(pDemod, DTMB_SOFT_RST_N, 0x1) != FUNCTION_SUCCESS)
		goto error_status_set_registers;


	return FUNCTION_SUCCESS;


error_status_set_registers:
	return FUNCTION_ERROR;
}





