int
realtek_r_dtmb_SetBandwidthMode(
	DTMB_DEMOD_MODULE *pDemod,
	int BandwidthMode
)
{

	U32BITS res_ratio_value;


	// Determine PRIVATE_0 and REG_0 value according to bandwidth mode.
	switch (BandwidthMode) {
	case DTMB_BANDWIDTH_6MHZ:
		res_ratio_value = 0x10EE642;
		break;
	case DTMB_BANDWIDTH_7MHZ:
		res_ratio_value = 0xe83315;
		break;
	case DTMB_BANDWIDTH_8MHZ:
		res_ratio_value = 0xcb2cb2;
		break;
	default:
		goto error_status;
	}

	// Set IFFREQ with calculated value.
	if (pDemod->RegAccess.Addr16Bit.SetRegBits(pDemod, DTMB_RES_RATIO, res_ratio_value) != FUNCTION_SUCCESS)
		goto error_status;

	pDemod->SoftwareReset(pDemod);


	// Store demod setting parameters.
	pDemod->BandwidthMode      = BandwidthMode;
	pDemod->IsBandwidthModeSet = YES;


	return FUNCTION_SUCCESS;


error_status:
	return FUNCTION_ERROR;
}




