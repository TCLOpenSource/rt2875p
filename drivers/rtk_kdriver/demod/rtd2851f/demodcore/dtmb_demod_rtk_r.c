/**

@see   DTMB_DEMOD_FP_SET_IF_FREQ_HZ

*/
int
realtek_r_dtmb_SetIfFreqHz(
	DTMB_DEMOD_MODULE *pDemod,
	U32BITS IfFreqHz
)
{

	U32BITS BbinEn = 0, EnDcr = 0;

	U32BITS IfFreqHzAdj = 0;

	MPI MpiVar, MpiNone, MpiConst;

	S32BITS IffreqInt = 0;
	U32BITS IffreqBinary = 0;

	memset(&MpiVar, 0, sizeof(MpiVar));
	memset(&MpiNone, 0, sizeof(MpiNone));
	memset(&MpiConst, 0, sizeof(MpiConst));



	// Determine and set BBIN_EN and EN_DCR value.
	BbinEn = (IfFreqHz == IF_FREQ_0HZ) ? 0x1 : 0x0;
	EnDcr  = (IfFreqHz == IF_FREQ_0HZ) ? 0x1 : 0x0;

	if (pDemod->RegAccess.Addr16Bit.SetRegBits(pDemod, DTMB_BBIN_EN, BbinEn) != FUNCTION_SUCCESS)
		goto error_status_set_registers;

	if (pDemod->RegAccess.Addr16Bit.SetRegBits(pDemod, DTMB_EN_DCR, EnDcr) != FUNCTION_SUCCESS)
		goto error_status_set_registers;


	// Calculate IFFREQ value.
	// Note: Case 1: IfFreqHz < 24000000,  IfFreqHzAdj = IfFreqHz;
	//       Case 2: IfFreqHz >= 24000000, IfFreqHzAdj = 48000000 - IfFreqHz;
	//       IFFREQ = - round( IfFreqHzAdj * pow(2, 10) / 48000000 )
	//              = - floor( (IfFreqHzAdj * pow(2, 10) + 24000000) / 48000000 )
	//       REALTEK_R_DTMB_ADC_FREQ_HZ = 48 MHz
	//       IFFREQ_BIT_NUM = 10
	IfFreqHzAdj = (IfFreqHz < (REALTEK_R_DTMB_ADC_FREQ_HZ / 2)) ? IfFreqHz : (REALTEK_R_DTMB_ADC_FREQ_HZ - IfFreqHz);

	MpiSetValue(&MpiVar, IfFreqHzAdj);
	MpiLeftShift(&MpiVar, MpiVar, REALTEK_R_DTMB_IFFREQ_BIT_NUM);

	MpiSetValue(&MpiConst, (REALTEK_R_DTMB_ADC_FREQ_HZ / 2));
	MpiAdd(&MpiVar, MpiVar, MpiConst);

	MpiSetValue(&MpiConst, REALTEK_R_DTMB_ADC_FREQ_HZ);
	MpiDiv(&MpiVar, &MpiNone, MpiVar, MpiConst);

	MpiGetValue(MpiVar, &IffreqInt);
	IffreqInt = - IffreqInt;

	IffreqBinary = SignedIntToBin(IffreqInt, REALTEK_R_DTMB_IFFREQ_BIT_NUM);


	// Set IFFREQ with calculated value.
	if (pDemod->RegAccess.Addr16Bit.SetRegBits(pDemod, DTMB_IFFREQ, IffreqBinary) != FUNCTION_SUCCESS)
		goto error_status_set_registers;


	// Set demod IF frequnecy parameter.
	pDemod->IfFreqHz      = IfFreqHz;
	pDemod->IsIfFreqHzSet = YES;


	return FUNCTION_SUCCESS;


error_status_set_registers:
	return FUNCTION_ERROR;
}





